void petscii() {
    unsigned int i;
    unsigned long int j;  
    // Output each character to screen memory
    unsigned char screen_chars[256];
    bgcolor(0);
    bordercolor(0); //black
    textcolor(7); //yellow
    clrscr();

    // clear the screen with char 0
    for (i = 0; i < 1000; i++) {
    ((unsigned char*)0xC800)[i] = 0;
    }  
  
    // draw 256 chars from the set
    for(j=0;j<256;j++) {screen_chars[j] = j;
    memcpy((void*)0xC800, screen_chars, 256);
    }
}




